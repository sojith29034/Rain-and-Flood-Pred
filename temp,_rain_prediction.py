# Store metrics
model_metrics['Logistic Regression'] = {
    'Accuracy': accuracy_score(y_test, log_reg_pred),
    'Precision': precision_score(y_test, log_reg_pred),
    'Recall': recall_score(y_test, log_reg_pred),
    'F1 Score': f1_score(y_test, log_reg_pred)
}

